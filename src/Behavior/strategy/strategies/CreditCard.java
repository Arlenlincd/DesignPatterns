package Behavior.strategy.strategies;

/**
 * @ClassName: CreditCard
 * @Description: 信用卡类
 * @Author: arlin
 * @Date: 2021/6/27
 */
public class CreditCard {
    private int amount;
    private String number;
    private String date;
    private String cvv;

    CreditCard(String number, String date, String cvv) {
        this.amount = 100_000;
        this.number = number;
        this.date = date;
        this.cvv = cvv;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public int getAmount() {
        return amount;
    }
}
